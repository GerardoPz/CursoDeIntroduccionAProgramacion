package com.mycompany.ejerciciotema9;

/**
 *
 * @author gerar
 */
public class Persona {
    int edad;
    String nombre;
    int telefono;

    public Persona(int edad, String nombre, int telefono) {
        this.edad = edad;
        this.nombre = nombre;
        this.telefono = telefono;
    }

    public int getEdad() {
        return edad;
    }

    public String getNombre() {
        return nombre;
    }

    public int getTelefono() {
        return telefono;
    }
    
    
}
